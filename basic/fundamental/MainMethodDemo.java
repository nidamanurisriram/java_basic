// Starting execution point of any program is main method.

// In java only the code in the main method is executed.

// Main method has standard signature and compiler will be identifying by the signature.

// In java main method statement is : public static void main ( String [] args ) { .... }.

// public -- access modifier.

// static -- access modifier.

// void -- return type.

// main -- method name.

// (String[] args) -- it stores the java command line arguments and used to execute the program.

package fundamental;

public class MainMethodDemo {
	
	public static void main(String[] args) {
		
		System.out.println("Main Method ...");
		
	}

}
