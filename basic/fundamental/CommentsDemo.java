// In java comments are non executable statements.

// In general comments are used for readable purpose.

// In java comments are done in two ways 1. Single line comments 2. Multi line comments.

// 1. Single line comments 

// Syntax : // data

// Using single line comments (// data) procedure comments are made for one line.  

// 2. Multi line comments

// Syntax : /* data */

// Using multi line comments (/* data */) procedure comments are made multiple for lines. 

package fundamental;

public class CommentsDemo {
	
	public static void main(String[] args) {
		
		// Hi
		
		/*
		 * 1
		 * 2
		 * 3
		 * 4
		 * 5
		 */
		
		System.out.println("// (data) is for single line comments.");
		
		System.out.println("/* (data) */ is for multi line comments");
		
	}

}
